   &nbsp;&nbsp;SWAP<br> 
   END<br>			
   OVER<br>
   MOD<span><i class=commentaire>// - Application d'un algorithme utilisant un théorème d'Euclide:</i></span><br>
   DUP<span><i class=commentaire> //   On étudie la valeur du reste de la division entière entre les deux nombres présents au niveau 2 et 1 de la pile :</i></span><br>
   IF 0 &gt;<br>
   THEN<span><i class=commentaire>//   - <b>si</b> il est supérieur à zéro, on rappelle récursivement le programme avec les 2 éléments présents sur la pile,</i></span><br>
   &nbsp;&nbsp;PGCD<br>
   ELSE<span><i class=commentaire>//   - <b>sinon</b> le nombre obtenu au niveau 2 est le pgcd des 2 nombres.</i></span><br>                            
