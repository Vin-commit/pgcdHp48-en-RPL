   <u><b>Lexique des instructions employées par ordre d'utilisation :</b></u><br>
   <br>

   DUP2 :<br><span>Duplique 2 éléments :</span><br>
   <span>- copie celui sur le niveau 1 vers le niveau 3</span><br>
   <span>- copie celui sur le niveau 2 vers le niveau 4</span><br><br>
  
   - :<br><span>Soustrait 2 éléments : elt. niv. 2 - elt. niv. 1</span><br><br>

   IF 0 &gt; :<br><span>Compare la valeur de l'elt. sur le niv.1 avec 0 et suivant celle-ci poursuit :</span><br>
   <span>- soit avec les instructions après THEN,</span><br>
   <span>- soit avec les instructions après ELSE.</span><br><br>
        
   THEN :<br><span>Instructions pouvant être exécutées après un IF.</span><br><br>

   SWAP :<span><br><span>Echange la position entre l'elt. sur le niv. 1 et celui sur le niv. 2</span></span><br><br>
 
   END :<br><span>Fin du bloc (ici conditionnel) des instructions commençant par le dernier IF (ici).</span><br><br>

   OVER :<br><span>Copie l'elt. du niv. 2 (qui deviendra après le niveau 3) vers le niv. 1.</span><br><br>

   MOD :<br><span>Effectue le modulo entre les elts. du niv. 2 et du niv. 1</span><br><br>

   DUP :<br><span>Duplique 1 élément :</span><br>
   <span>- copie celui sur le niv. 1 vers le niv. 2 (celui s'y trouvant avant est déplacé vers le niv. supérieur).</span><br><br>

   PGCD :<br><span>Ici nom du programme appelé.</span><br><br>

   ELSE :<br><span>Instructions pouvant être exécutées après un IF (ici).</span><br><br>

   DROP :<br><span>Supprime l'elt. du niv.1 (les autres sont donc décalés de 1 niv.).</span><br><br>

   "PGCD(1,2) : " :<br><span>Il s'agit d'une chaîne de caractères placée sur le niv. 1.</span><br><br>

   + :<br><span>Ici, concaténation de l'elt. sur le niv. 1 (un nombre) avec l'elt. sur le niv. 2 (une chaîne).</span>
